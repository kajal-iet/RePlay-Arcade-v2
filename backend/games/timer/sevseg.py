DIGITS = {
    '0': [" __ ", "|  |", "|__|"],
    '1': ["    ", "   |", "   |"],
    '2': [" __ ", " __|", "|__ "],
    '3': [" __ ", " __|", " __|"],
    '4': ["    ", "|__|", "   |"],
    '5': [" __ ", "|__ ", " __|"],
    '6': [" __ ", "|__ ", "|__|"],
    '7': [" __ ", "   |", "   |"],
    '8': [" __ ", "|__|", "|__|"],
    '9': [" __ ", "|__|", " __|"],
}

def get_sevseg_str(num: str, width: int):
    num = num.zfill(width)
    lines = ["", "", ""]
    for ch in num:
        d = DIGITS.get(ch, ["    ", "    ", "    "])
        for i in range(3):
            lines[i] += d[i] + " "
    return "\n".join(lines)
